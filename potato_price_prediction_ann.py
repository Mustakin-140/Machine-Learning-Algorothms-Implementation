#ANN model building
model=tf.keras.Sequential([
    tf.keras.layers.Dense(1,input_shape=[1], activation='linear')
])

model.compile(optimizer='sgd',
              loss='mean_squared_error',
              metrics=['accuracy'])

#Training model
model.fit(X_train,Y_train, epochs=500)

#predict
