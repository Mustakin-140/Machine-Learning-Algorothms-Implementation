df=pd.read_csv('Potato_Price.csv')

X=df[['Potato_kg']]
Y=df['Price']

print(X)

print(Y)

from sklearn.model_selection import train_test_split
X_train,X_test,Y_train,Y_test=train_test_split(X,Y, test_size=0.3)#7 will be trained, 3 will be tested

